package com.ruoyi.zxydk.filterchain.annotation;

import java.lang.annotation.Documented;
import java.lang.annotation.Retention;
import java.lang.annotation.RetentionPolicy;
import java.lang.annotation.Target;

@Documented
@Target({java.lang.annotation.ElementType.TYPE})
@Retention(RetentionPolicy.RUNTIME)
public @interface CommandOrder {
	
	/**
	 *  
	 *   sort for filter-command-chain 
	 *   
	 *   1 < 2 : 1:first ; 2 : secound
	 *   
	 */
	public int order();
	
	
	/**
	 *  base of spring transaction template for sub command transactions
	 *  
	 *   defaut false;
	 */
	public boolean isOpenSubTransaction() default false;
}
